function updateOtherCountryField() {
  const showField = countrySelect.value === "Other";
  otherCountryField.hidden = !showField;
  otherCountryInput.required = showField;

  if (!showField) otherCountryInput.value = "";
}

function setLanguage(language) {
  currentLanguage = language;
  document.documentElement.lang = language === "zh" ? "zh-CN" : "en";
  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const value = translations[language][element.dataset.i18n];
    if (value) element.innerHTML = value;
  });
  document.querySelectorAll("[data-i18n-placeholder]").forEach((element) => {
    const value = translations[language][element.dataset.i18nPlaceholder];
    if (value) element.placeholder = value;
  });
  languageToggle.textContent = language === "en" ? "中文" : "EN";
  localStorage.setItem("sunever-language", language);
  renderChatMessages();
}

function getChatMessages() {
  try { return JSON.parse(localStorage.getItem("sunever-chat-messages")) || []; }
  catch { return []; }
}

function saveChatMessages(messages) {
  localStorage.setItem("sunever-chat-messages", JSON.stringify(messages.slice(-50)));
}

function messageTime(timestamp) {
  return new Intl.DateTimeFormat(currentLanguage === "zh" ? "zh-CN" : "en", { hour: "2-digit", minute: "2-digit" }).format(timestamp);
}

function renderChatMessages() {
  if (!chatMessages) return;
  const messages = getChatMessages();
  chatMessages.replaceChildren();
  const displayMessages = messages.length ? messages : [{ id: "welcome", sender: "team", textKey: "chat.welcome", timestamp: Date.now() }];
  displayMessages.forEach((message) => {
    const item = document.createElement("div");
    item.className = `chat-message ${message.sender === "user" ? "user" : "team"}`;
    const bubble = document.createElement("div");
    bubble.className = "chat-bubble";
    bubble.textContent = message.textKey ? translations[currentLanguage][message.textKey] : message.text;
    const time = document.createElement("time");
    time.className = "chat-time";
    time.dateTime = new Date(message.timestamp).toISOString();
    time.textContent = messageTime(message.timestamp);
    item.append(bubble, time);
    chatMessages.append(item);
  });
  chatMessages.scrollTop = chatMessages.scrollHeight;
}

function addChatMessage(message, announce = false) {
  const messages = getChatMessages();
  if (messages.some((item) => item.id === message.id)) return;
  messages.push(message);
  saveChatMessages(messages);
  renderChatMessages();
  if (announce && !chatWidget.classList.contains("open")) {
    unreadCount += 1;
    chatUnread.textContent = unreadCount;
    chatUnread.hidden = false;
  }
}

function setChatOpen(open) {
  chatWidget.classList.toggle("open", open);
  chatPanel.setAttribute("aria-hidden", String(!open));
  chatLauncher.setAttribute("aria-expanded", String(open));
  if (open) {
    unreadCount = 0;
    chatUnread.hidden = true;
    window.setTimeout(() => chatInput.focus(), 220);
  } else chatLauncher.focus();
}

function simulateTeamReply() {
  chatTyping.textContent = translations[currentLanguage]["chat.typing"];
  window.setTimeout(() => {
    chatTyping.textContent = "";
    const reply = { id: `team-${Date.now()}`, sender: "team", textKey: "chat.reply", timestamp: Date.now() };
    addChatMessage(reply, true);
    chatChannel?.postMessage(reply);
  }, 1100);
}

function connectLiveChat() {
  if (!chatSocketEndpoint) return;
  chatSocket = new WebSocket(chatSocketEndpoint);
  chatSocket.addEventListener("message", (event) => {
    try {
      const incoming = JSON.parse(event.data);
      addChatMessage({
        id: incoming.id || `team-${Date.now()}`,
        sender: incoming.sender || "team",
        text: incoming.text,
        timestamp: incoming.timestamp || Date.now()
      }, true);
    } catch { /* Ignore malformed service messages. */ }
  });
}

if (chatWidget) {
  chatLauncher.addEventListener("click", () => setChatOpen(!chatWidget.classList.contains("open")));
  chatClose.addEventListener("click", () => setChatOpen(false));
  chatForm.addEventListener("submit", (event) => {
    event.preventDefault();
    const text = chatInput.value.trim();
    if (!text) return;
    const message = { id: `user-${Date.now()}`, sender: "user", text, timestamp: Date.now() };
    addChatMessage(message);
    chatChannel?.postMessage(message);
    if (chatSocket?.readyState === WebSocket.OPEN) chatSocket.send(JSON.stringify(message));
    chatInput.value = "";
    chatInput.style.height = "auto";
    if (!chatSocketEndpoint) simulateTeamReply();
  });
  chatInput.addEventListener("input", () => {
    chatInput.style.height = "auto";
    chatInput.style.height = `${Math.min(chatInput.scrollHeight, 90)}px`;
  });
  chatInput.addEventListener("keydown", (event) => {
    if (event.key === "Enter" && !event.shiftKey) {
      event.preventDefault();
      chatForm.requestSubmit();
    }
  });
  chatChannel?.addEventListener("message", (event) => addChatMessage(event.data, event.data.sender === "team"));
  connectLiveChat();
}

function closeMenu() {
  menu.classList.remove("open");
  document.body.classList.remove("menu-open");
  menuToggle.setAttribute("aria-expanded", "false");
}

menuToggle.addEventListener("click", () => {
  const willOpen = !menu.classList.contains("open");
  menu.classList.toggle("open", willOpen);
  document.body.classList.toggle("menu-open", willOpen);
  menuToggle.setAttribute("aria-expanded", String(willOpen));
});

